public class pessoa {
    private String nome;
    private int idade;
    private double altura;

    public void setName(String name){
         this.nome = name;
    }
    public String getName(){
        return this.nome;
    }
    public void setIdade(int idade){
        this.idade = idade;
    }
    public int getIdade(){
        return this.idade;
    }
    public void setAltura(double altura){
        this.altura = altura;
    }
    public double getAltura(){
        return this.altura;
    }

}
